""""
    布尔值也可以用and、or、not运算
    and -> 与
    or -> 或
    not -> 非（不是）
"""
print(True and True)
print(True and False)
print(False and False)
print("----------------------")
print(True or True)
print(True or False)
print(False or False)
print("----------------------")
print(not False)
print(not True)